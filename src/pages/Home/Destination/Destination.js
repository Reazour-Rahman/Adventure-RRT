import React from 'react';
import { useTheme } from '@mui/material/styles';
import Box from '@mui/material/Box';
import MobileStepper from '@mui/material/MobileStepper';
import Paper from '@mui/material/Paper';
import Typography from '@mui/material/Typography';
import Button from '@mui/material/Button';
import KeyboardArrowLeft from '@mui/icons-material/KeyboardArrowLeft';
import KeyboardArrowRight from '@mui/icons-material/KeyboardArrowRight';
import SwipeableViews from 'react-swipeable-views';
import { autoPlay } from 'react-swipeable-views-utils';
import img1 from "../../../images/1.jpg"
import img2 from "../../../images/2.jpg"
import img3 from "../../../images/3.jpg"
import img4 from "../../../images/4.jpg"


const AutoPlaySwipeableViews = autoPlay(SwipeableViews);

const images = [
    {
      label: 'Bali, Indonesia',
      imgPath:`${img1}`,
    },
    {
      label: 'Xingian , destad Naddesa, China',
      imgPath:`${img2}`,
    },
    {
      label: 'Falkenberg, Alingsås Sweden',
      imgPath:`${img3}`,
    },
    {
      label: 'Toronto, Canada Republic',
      imgPath:`${img4}`,
    },
  ];

const Destination = () => {

    const theme = useTheme();
    const [activeStep, setActiveStep] = React.useState(0);
    const maxSteps = images.length;

    const handleNext = () => {
        setActiveStep((prevActiveStep) => prevActiveStep + 1);
    };

    const handleBack = () => {
        setActiveStep((prevActiveStep) => prevActiveStep - 1);
    };

    const handleStepChange = (step) => {
        setActiveStep(step);
    };

    return (
        <section style={{backgroundColor:"#EAEEF3" , paddingLeft:"22px", paddingRight:"22px"}}>
        <div style={{}}>
            <div className="pb-4 pt-5">
                <h1 className="text-success">Where to Go... No Idea ?</h1>
                <p className="text-center">Explore our four regions:</p>
            </div>
            <div>
            <Box  sx={{ flexGrow: 1 }}>
                <Paper
                    square
                    elevation={0}
                    sx={{
                    display: 'flex',
                    alignItems: 'center',
                    height: 50,
                    pl: 2,
                    bgcolor: 'background.default',
                    }}
                >
                    <Typography>{images[activeStep].label}</Typography>
                </Paper>
                <AutoPlaySwipeableViews
                    axis={theme.direction === 'rtl' ? 'x-reverse' : 'x'}
                    index={activeStep}
                    onChangeIndex={handleStepChange}
                    enableMouseEvents
                >
                    {images.map((step, index) => (
                    <div key={step.label}>
                        {Math.abs(activeStep - index) <= 2 ? (
                        <Box
                            component="img"
                            sx={{
                            height: 600,
                            display: 'block',                
                            overflow: 'hidden',
                            width: '100%',
                            }}
                            src={step.imgPath}
                            alt={step.label}
                        />
                        ) : null}
                    </div>
                    ))}
                </AutoPlaySwipeableViews>
                <MobileStepper
                    steps={maxSteps}
                    position="static"
                    activeStep={activeStep}
                    nextButton={
                    <Button
                        size="small"
                        onClick={handleNext}
                        disabled={activeStep === maxSteps - 1}
                    >
                        Next
                        {theme.direction === 'rtl' ? (
                        <KeyboardArrowLeft />
                        ) : (
                        <KeyboardArrowRight />
                        )}
                    </Button>
                    }
                    backButton={
                    <Button size="small" onClick={handleBack} disabled={activeStep === 0}>
                        {theme.direction === 'rtl' ? (
                        <KeyboardArrowRight />
                        ) : (
                        <KeyboardArrowLeft />
                        )}
                        Back
                    </Button>
                    }
                />
            </Box>
            </div>
        </div>
        </section>
    );
};

export default Destination;

